package com.hemajoo.commerce.cherry.server.document.repository;


import com.hemajoo.commerce.cherry.server.data.model.document.ServerDocument;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.JpaSpecificationExecutor;

import java.util.List;
import java.util.UUID;

/**
 * Repository for the <b>document</b> entities.
 * @author <a href="mailto:christophe.resse@gmail.com">Christophe Resse</a>
 * @version 1.0.0
 */
public interface IDocumentRepository extends JpaRepository<ServerDocument, UUID>, JpaSpecificationExecutor<ServerDocument>
{
    /**
     * Returns a list of documents belonging to the given parent identifier.
     * @param id Parent identifier.
     * @return List of documents.
     */
    List<ServerDocument> findByParentId(UUID id);
}
